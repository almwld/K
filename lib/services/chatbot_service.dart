  static String? getResponse(String message) {
    final lowerMessage = message.toLowerCase();
    
    // البحث عن إجابة مطابقة
    for (var entry in _knowledgeBase.entries) {
      if (lowerMessage.contains(entry.key.toLowerCase())) {
        return entry.value;
      }
    }
    
    // كلمات مفتاحية عامة
    if (lowerMessage.contains('مرحب') || lowerMessage.contains('السلام')) {
      return 'وعليكم السلام! 👋 كيف يمكنني مساعدتك اليوم؟';
    }
    if (lowerMessage.contains('شكر')) {
      return 'العفو! 🌟 دائماً في خدمتك. هل هناك شيء آخر يمكنني مساعدتك به؟';
    }
    if (lowerMessage.contains('باي') || lowerMessage.contains('مع السلامة')) {
      return 'مع السلامة! 👋 نتمنى لك يوماً سعيداً. عد في أي وقت.';
    }
    
    // إذا لم يجد إجابة
    return null;
  }
  
  static bool canAnswer(String message) {
    return getResponse(message) != null;
